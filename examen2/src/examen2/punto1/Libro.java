package examen2.punto1;

/**
 *
 * @author Usuario
 */
public class Libro {
    String titulo;
    String autor;
    String año;
    double precio;

    public Libro(String titulo, String autor, String año, double precio) {
        this.titulo = titulo;
        this.autor = autor;
        this.año = año;
        this.precio = precio;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getAño() {
        return año;
    }

    public void setAño(String año) {
        this.año = año;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
    
    
}
